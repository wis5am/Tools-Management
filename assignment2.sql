CREATE OR REPLACE VIEW CustomerAddressView AS
SELECT
    A.addressId,
    C.countryID,
    C.Country,
    A.City,
    A.Province,
    A.PostalCode,
    A.Street,
    CAA.StartDate,
    CAA.EndDate
FROM
    CustomerAddressAssoc CAA
JOIN
    country C ON CAA.countryID = C.countryID
JOIN
    address A ON CAA.addressId = A.addressId
where CAA.EndDate is null;
    -- -------------------------------
    CREATE OR REPLACE VIEW CustomerNameHistoryView AS
SELECT
    N.NameId,
    C.CustomerID,
    C.AddressID,
    N.Name,
    CNH.StartTime,
    CNH.EndTime
FROM
    CustomerNameHistory CNH
JOIN
    Customers C ON CNH.CustomerID = C.CustomerID
JOIN
    Names N ON CNH.NameId = N.NameId
    where CNH.EndTime is null;
