create table country(
    countryID NUMBER GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1) NOT NULL,
    Country varchar(100),
    PRIMARY KEY (countryID)
);
CREATE TABLE address (
    addressId NUMBER GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1) NOT NULL,    
    City VARCHAR2(100),
    Province VARCHAR2(100),
    PostalCode VARCHAR2(20),
    Street VARCHAR2(255),
    CONSTRAINT PK_AddressHistory PRIMARY KEY (addressId)
);
CREATE TABLE CustomerAddressAssoc (
    CustomerAddressAssoc NUMBER GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1) NOT NULL,
    countryID NUMBER,
    addressId NUMBER,
    StartDate DATE NOT NULL,
    EndDate DATE,
    CONSTRAINT PK_CustomerAddressAssoc PRIMARY KEY (CustomerAddressAssoc),
    CONSTRAINT FK_CustomerAddressAssoc_addressId FOREIGN KEY (addressId) REFERENCES address (addressId) ON DELETE CASCADE,
    CONSTRAINT FK_CustomerAddressAssoc_countryID FOREIGN KEY (countryID) REFERENCES country (countryID) ON DELETE CASCADE
);
CREATE TABLE Customers (
    CustomerID NUMBER GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1) NOT NULL,
    AddressID NUMBER,
    PRIMARY KEY (CustomerID),
    CONSTRAINT FK_Customers_AddressID FOREIGN KEY (AddressID) REFERENCES Address (addressID) ON DELETE CASCADE
);
CREATE TABLE Names (
    NameId NUMBER GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1) NOT NULL,
    Name varchar(100) NOT NULL,
    PRIMARY KEY (NameId)
);
CREATE TABLE CustomerNameHistory (
    HistoryID NUMBER GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1) NOT NULL,
    CustomerID NUMBER NOT NULL,
    NameId NUMBER NOT NULL,
    Name VARCHAR2(100),
    StartTime TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    EndTime TIMESTAMP,
    PRIMARY KEY (HistoryID),
    FOREIGN KEY (CustomerID) REFERENCES Customers (CustomerID) ON DELETE CASCADE,
    FOREIGN KEY (NameId) REFERENCES Names (NameId) ON DELETE CASCADE
);
CREATE TABLE Tools (
    ToolID NUMBER GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1) NOT NULL,
    Name VARCHAR2(100),
    Description VARCHAR2(255),
    RentalPrice NUMBER(10, 2),
    PurchaseDate DATE,
    Manufacturer VARCHAR2(100),
    Specifications VARCHAR2(255),
    Instructions VARCHAR2(255),
    PRIMARY KEY (ToolID)
);
CREATE TABLE Rentals (
    RentalID NUMBER GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1) NOT NULL,
    RentalStatus VARCHAR2(50),
    PRIMARY KEY (RentalID)
);
CREATE TABLE ToolsRented (
    ToolsRentedId NUMBER GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1) NOT NULL,
    ToolID NUMBER NOT NULL,
    RentalID NUMBER NOT NULL,
    RentalStartDate DATE not null,
    RentalEndDate DATE,
    PRIMARY KEY (ToolsRentedId),
    CONSTRAINT FK_ToolsRented_ToolID FOREIGN KEY (ToolID) REFERENCES Tools (ToolID) ON DELETE CASCADE,
    CONSTRAINT FK_ToolsRented_RentalID FOREIGN KEY (RentalID) REFERENCES Rentals (RentalID) ON DELETE CASCADE
);
CREATE TABLE customerRentals (
    CustomerID NUMBER NOT NULL,
    RentalID NUMBER NOT NULL,
    PRIMARY KEY (CustomerID, RentalID),
    CONSTRAINT FK_customerRentals_Customers FOREIGN KEY (CustomerID) REFERENCES Customers (CustomerID) ON DELETE CASCADE,
    CONSTRAINT FK_customerRentals_Rentals FOREIGN KEY (RentalID) REFERENCES Rentals (RentalID) ON DELETE CASCADE
);
CREATE TABLE Employees (
    EmployeeID NUMBER GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1) NOT NULL,
    FirstName VARCHAR2(100),
    LastName VARCHAR2(100),
    PhoneNumber VARCHAR2(20),
    Email VARCHAR2(100),
    JobTitle VARCHAR2(100),
    PRIMARY KEY (EmployeeID)
);
CREATE TABLE MaintenanceLogs (
    ToolID NUMBER,
    EmployeeID NUMBER,
    MaintenanceDate DATE,
    MaintenanceDescription VARCHAR2(255),
    CONSTRAINT FK_MaintenanceLogs_ToolID FOREIGN KEY (ToolID) REFERENCES Tools (ToolID) ON DELETE CASCADE,
    CONSTRAINT FK_MaintenanceLogs_EmployeeID FOREIGN KEY (EmployeeID) REFERENCES Employees (EmployeeID) ON DELETE CASCADE
);
CREATE TABLE RentalPayments (
    PaymentID NUMBER NOT NULL,
    RentalID NUMBER,
    PaymentDate DATE,
    PaymentAmount NUMBER(10, 2),
    PaymentMethod VARCHAR2(50),
    PRIMARY KEY (PaymentID),
    CONSTRAINT FK_RentalPayments_RentalID FOREIGN KEY (RentalID) REFERENCES Rentals (RentalID) ON DELETE CASCADE
);
CREATE TABLE Categories (
    CategoryID NUMBER GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1) NOT NULL,
    name VARCHAR2(250),
    Description VARCHAR2(250),
    PRIMARY KEY (CategoryID)
);
CREATE TABLE toolsCategories (
    categoryID NUMBER,
    toolID NUMBER,
    CONSTRAINT FK_toolsCategories_categoryID FOREIGN KEY (categoryID) REFERENCES Categories (CategoryID) ON DELETE CASCADE,
    CONSTRAINT FK_toolsCategories_toolID FOREIGN KEY (toolID) REFERENCES Tools (ToolID) ON DELETE CASCADE
);
